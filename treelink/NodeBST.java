public class NodeBST<E> {
    protected E data;
    protected NodeBST<E> right;
    protected NodeBST<E> left;

    public NodeBST(E data) {
        this(data, null, null);
    }

    public NodeBST(E data, NodeBST<E> right, NodeBST<E> left) {
        this.data = data;
        this.right = right;
        this.left = left;
    }

    // Getter y setter para data
    public E getData() {
        return data;
    }

    public void setData(E data) {
        this.data = data;
    }

    // Getter y setter para right
    public NodeBST<E> getRight() {
        return right;
    }

    public void setRight(NodeBST<E> right) {
        this.right = right;
    }

    // Getter y setter para left
    public NodeBST<E> getLeft() {
        return left;
    }

    public void setLeft(NodeBST<E> left) {
        this.left = left;
    }

    public String toString() {
        return data.toString();
    }
}
